package Clases;

/**
 *
 * @author Yuli
 */
public class Cliente {
    private int DNI;
    private String nombre;
    private String apellido;
    private int puntos;

    public Cliente() {
    }

    public Cliente(int DNI, String nombre, String apellido, int puntos) {
        this.DNI = DNI;
        this.nombre = nombre;
        this.apellido = apellido;
        this.puntos = puntos;
    }

    public int getDNI() {
        return DNI;
    }

    public void setDNI(int DNI) {
        this.DNI = DNI;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public int getPuntos() {
        return puntos;
    }

    public void setPuntos(int puntos) {
        this.puntos = puntos;
    }
    
    
}
